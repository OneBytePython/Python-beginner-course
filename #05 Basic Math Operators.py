# Basic Math Operators in Python

# Addition
print(3 + 2)     # 5

# Subtraction
print(5 - 1)     # 4

# Multiplication
print(4 * 2)     # 8

# Division (always returns float!)
print(9 / 3)     # 3.0

# Integer (floor) division
print(7 // 2)   # 3 — because 7 ÷ 2 = 3.5 → floor is 3
print(-7 // 2)  # -4 — it rounds down toward minus infinity

# Modulo (remainder)
print(7 % 2)    # 1 — because 7 = 2*3 + 1
print(10 % 5)   # 0 — because 10 is divisible by 5

# Exponentiation (power)
print(2 ** 3)   # 8 — because 2 * 2 * 2 = 8
print(5 ** 0)   # 1 — any number to the power of 0 is 1

import math
print(math.sqrt(16))  # Output: 4.0
